const { Mongoose } = require("../configs");

const categorySchema = new Mongoose.Schema({
  name: {
    type: String,
    required: true,
    unique: true,
  },
  image: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

const Category = Mongoose.model("Category", categorySchema);

module.exports = Category;
